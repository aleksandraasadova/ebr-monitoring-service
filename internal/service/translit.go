package service

import "strings"

// toTransliteratedLower переводит русские буквы в латиницу (нижний регистр)
// Пример: "Иванов" → "ivanov", "Жуков" → "zhukov"
func toTransliteratedLower(s string) string {
	// strings.Replacer заменяет подстроки по списку пар "что" → "на что"
	replacer := strings.NewReplacer(
		"а", "a", "А", "a",
		"б", "b", "Б", "b",
		"в", "v", "В", "v",
		"г", "g", "Г", "g",
		"д", "d", "Д", "d",
		"е", "e", "Е", "e",
		"ё", "e", "Ё", "e",
		"ж", "zh", "Ж", "zh",
		"з", "z", "З", "z",
		"и", "i", "И", "i",
		"й", "y", "Й", "y",
		"к", "k", "К", "k",
		"л", "l", "Л", "l",
		"м", "m", "М", "m",
		"н", "n", "Н", "n",
		"о", "o", "О", "o",
		"п", "p", "П", "p",
		"р", "r", "Р", "r",
		"с", "s", "С", "s",
		"т", "t", "Т", "t",
		"у", "u", "У", "u",
		"ф", "f", "Ф", "f",
		"х", "h", "Х", "h",
		"ц", "ts", "Ц", "ts",
		"ч", "ch", "Ч", "ch",
		"ш", "sh", "Ш", "sh",
		"щ", "shch", "Щ", "shch",
		"ъ", "", "Ъ", "",
		"ы", "y", "Ы", "y",
		"ь", "", "Ь", "",
		"э", "e", "Э", "e",
		"ю", "yu", "Ю", "yu",
		"я", "ya", "Я", "ya",
	)

	// 1. Заменяем буквы
	// 2. Приводим к нижнему регистру
	return strings.ToLower(replacer.Replace(s))
}
